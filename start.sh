ascii='''\033[1;31m
                `.-..........`
               `////////::.`-/. 	
               -: ....-////////.
               //:-::///////////`
        `--::: `-://////////////:
        //////-    ``.-:///////// .`
        `://////:-.`    :///////::///:`
        .-///////////:---/////////////:
           .-:////////////////////////.
          \033[0;37myMN+`.-\033[1;31m:://///////////////-`\033[0;37m
       .-`:NMMNMs`  `..---------..`
        MN+/mMMMMMhoooyysshsss
  MMM   MMMMMMMMMMMMMMyyddMMM+
   MMMM  MMMMMMMMMMMMMNdyNMMH`      hyhMMM
    MMMMMMMMMMMMMMMyoNNNMMM+.    MMMMMMMM
     MMNMMMNNMMMMMNM+ mhsMNyyyyMNMMMMsMM
'''

tinyascii='''\033[1;31m
\033[1;31m          `.-..........`          
\033[1;31m         `////////::.`-/.         
\033[1;31m         -: ....-////////.        
\033[1;31m         //:-::///////////`       
\033[1;31m  `--::: `-://////////////:       
\033[1;31m  //////-    ``.-:///////// .`    
\033[1;31m  `://////:-.`    :///////::///:` 
\033[1;31m  .-///////////:---/////////////: 
\033[1;31m     .-:////////////////////////. 
\033[1;31m           :://///////////////-`  

'''

info="""
  \033[0m▌
  \033[0m▌    \033[1;90mUSER    \033[1;33m>  \033[0;37m$(whoami | sed 's/.*/\L&/; s/[a-z]*/\u&/g')
  \033[0m▌    \033[1;90mOS      \033[1;33m>  \033[0;37m$(cat /etc/os-release | grep -oP 'PRETTY_NAME="\K[^"]+')
  \033[0m▌    \033[1;90mKERNEL  \033[1;33m>  \033[0;37m$(uname -r | sed 's/_.*//')
  \033[0m▌    \033[1;90mWM      \033[1;33m>  \033[0;37mXmonad
  \033[0m▌    \033[1;90mSHELL   \033[1;33m>  \033[0;37m$(basename $SHELL | sed 's/.*/\L&/; s/[a-z]*/\u&/g')
  \033[0m▌    \033[1;90mPKG     \033[1;33m>  \033[0;37m$(xbps-query -l | wc -l)
  \033[0m▌
  \033[0m▌    \033[0;31m██\033[0;33m██\033[0;32m██\033[0;36m██\033[0;34m██\033[0;35m██\033[0;37m██\033[0;30m██
  \033[0m▌
"""

print_small () {
	paste -d " " <(printf %b "$tinyascii") <(printf %b "$info")
}

print_big () {
	echo -e "$ascii"
}

print_hat () {
	echo -e "$tinyascii"
}

case "$1" in
	"ascii") print_big;;
	"hat") print_hat;;
	"") print_small;;
esac
